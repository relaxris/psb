    public void hitung(){
    int total = 0;
    for (int i = 0; i < tbltransaksi.getRowCount(); i++) {
        int amount = Integer.valueOf(tbltransaksi.getValueAt(i, 2).toString());
        total += amount;
    }
    txttotal.setText(Integer.toString(total));
    }
    

